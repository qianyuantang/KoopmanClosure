# Fig. S1
# ============================================================

def detect_all_peaks_and_valleys(signal):
    """
    Detect maxima and minima from the observed x signal for Fig. S1.
    """
    prominence = PEAK_VALLEY_PROMINENCE_FACTOR * (np.max(signal) - np.min(signal))

    peaks_up, _ = find_peaks(
        signal,
        prominence=prominence,
        distance=PEAK_VALLEY_DISTANCE
    )

    peaks_down, _ = find_peaks(
        -signal,
        prominence=prominence,
        distance=PEAK_VALLEY_DISTANCE
    )

    all_peak_valley_extrema = np.sort(np.concatenate([peaks_up, peaks_down]))

    print(
        f"Detected maxima: {len(peaks_up)}, "
        f"minima: {len(peaks_down)}, "
        f"total extrema: {len(all_peak_valley_extrema)}"
    )

    return all_peak_valley_extrema


def interval_count_distribution(peak_valley_extrema_indices):
    """Return interval values and their interval_counts."""
    if len(peak_valley_extrema_indices) < 2:
        return np.array([], dtype=int), np.array([], dtype=int), np.array([], dtype=int)

    peak_valley_intervals = np.diff(peak_valley_extrema_indices).astype(int)
    interval_values, interval_counts = np.unique(peak_valley_intervals, return_counts=True)

    return peak_valley_intervals, interval_values, interval_counts


def plot_figS1_window_distribution(all_peak_valley_extrema):
    """Plot Fig. S1: peak-valley interval distribution."""
    peak_valley_intervals, interval_values, interval_counts = interval_count_distribution(all_peak_valley_extrema)
    selected_window_T = int(peak_valley_intervals.max()) if peak_valley_intervals.size > 0 else WINDOW_T

    fig = plt.figure(figsize=(6.2, 2.2), dpi=300, facecolor="white")
    ax = fig.add_axes([0.16, 0.32, 0.78, 0.58], facecolor="white")

    if interval_values.size > 0:
        ax.bar(
            interval_values,
            interval_counts,
            width=0.9,
            edgecolor="black",
            linewidth=0.6,
            alpha=0.90,
            color="black"
        )

        ax.axvline(
            WINDOW_T,
            linestyle="--",
            linewidth=1.1,
            color="black"
        )

        ymax = max(int(interval_counts.max()), 1)
        ax.set_ylim(0, ymax * 1.20)

        ax.set_xlabel(r"Peak-valley interval $\Delta k$ (samples)", fontsize=16)
        ax.set_ylabel("Count", fontsize=16)
        ax.tick_params(axis="both", labelsize=14)
        ax.xaxis.set_major_locator(MaxNLocator(nbins=5))
        ax.yaxis.set_major_locator(MaxNLocator(nbins=3))

    else:
        ax.text(
            0.5,
            0.5,
            "Not enough extrema",
            ha="center",
            va="center",
            fontsize=16,
            transform=ax.transAxes
        )
        ax.set_axis_off()

    paper_axes(ax)
    save_svg(fig, "figS1")
    plt.close(fig)

    print("Fig. S1 maximum peak-valley interval =", selected_window_T)
    print("Fig. S2 calculation step =", WINDOW_T)


