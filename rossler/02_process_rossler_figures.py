"""
02_process_rossler_figS1_figS2_original_xy.py

Process Rossler raw data and generate Fig. S1 and Fig. S2 using the same core
calculation as the uploaded original Rossler script.

Input
-----
rossler_raw_data.npz
    Generated by 01_generate_rossler_raw_data.py.

Output
------
rossler_figS_original_xy_svg/
    figS1.svg
    figS2.svg

Core calculation matched to the uploaded original script
-------------------------------------------------------
1. Use only observed x and y:
       observed_coordinate_matrix  = [X, Y]
       observed_velocity_matrix = [dX, dY]

2. Use:
       embedding_dimension = 4000 - (5 - 1) * 1
       delay = 1

3. Build Hankel matrices for x, y and dx/dt, dy/dt.

4. Select the retained SVD rank by cumulative sum of singular values:
       cumulative_sum >= 0.99 * total_sum

5. Reconstruct the Hankel matrix:
       reconstructed_observable_matrix_V = U_r @ diag(s_r) @ Vh_r

6. Use:
       observable_velocity_matrix_V_dot = observable_velocity_matrix_H_dot
       step = 316

7. Compute closure_defect_beta_values using the same sliding-window closure-defect loop.

8. Plot Fig. S2 as beta and hidden z reference.

Only SVG files are saved.
"""

import os
import shutil
import warnings

import numpy as np
import matplotlib.pyplot as plt
from scipy.signal import find_peaks
from matplotlib.ticker import MaxNLocator

warnings.filterwarnings("ignore")


# ============================================================
# Files
# ============================================================

RAW_DATA_FILE = "rossler_raw_data.npz"
OUTPUT_DIR = "rossler_figS"


# ============================================================
# Parameters matched to the uploaded original script
# ============================================================

ANALYSIS_START_INDEX = 2000
ANALYSIS_END_INDEX = 6000

DELAY_TAU = 1
OBSERVABLE_WINDOW_LENGTH = 4000 - (5 - 1) * DELAY_TAU

SVD_ENERGY_RATIO_BY_SINGULAR_VALUE_SUM = 0.99

WINDOW_T = 316

LOCAL_MAXIMUM_SEARCH_WINDOW = 50
LOCAL_MAXIMUM_BETA_THRESHOLD = 9.0

PEAK_VALLEY_PROMINENCE_FACTOR = 0.315
PEAK_VALLEY_DISTANCE = 20

# The uploaded original plotting line used t[0:...], but the manuscript figure
# uses the absolute segment time around 20-60. Keep absolute time by default.
USE_ABSOLUTE_SEGMENT_TIME = True


# ============================================================
# Figure style
# ============================================================

plt.rcParams["font.family"] = "Arial"
plt.rcParams["mathtext.fontset"] = "custom"
plt.rcParams["mathtext.rm"] = "Arial"
plt.rcParams["mathtext.it"] = "Arial:italic"
plt.rcParams["mathtext.bf"] = "Arial:bold"
plt.rcParams["axes.unicode_minus"] = False
plt.rcParams["svg.fonttype"] = "none"


