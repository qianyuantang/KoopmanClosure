def load_raw_data():
    """Load raw Rossler data."""
    if not os.path.exists(RAW_DATA_FILE):
        raise FileNotFoundError(
            f"{RAW_DATA_FILE} was not found. Run 01_generate_rossler_raw_data.py first."
        )
    return np.load(RAW_DATA_FILE)




def read_raw_array(raw_data, original_key, renamed_key):
    """Read original key first, then renamed key for compatibility."""
    if original_key in raw_data:
        return raw_data[original_key]
    if renamed_key in raw_data:
        return raw_data[renamed_key]
    raise KeyError(
        f"Neither '{original_key}' nor '{renamed_key}' is in the archive. "
        f"Available keys: {list(raw_data.keys())}"
    )


def reset_output_dir():
    """Create a clean SVG-only output directory."""
    if os.path.isdir(OUTPUT_DIR):
        shutil.rmtree(OUTPUT_DIR)
    os.makedirs(OUTPUT_DIR, exist_ok=True)


def save_svg(fig, file_stem):
    """Save one figure as SVG only."""
    path = os.path.join(OUTPUT_DIR, f"{file_stem}.svg")
    fig.savefig(path, format="svg", facecolor="white", transparent=False)
    print(f"Saved: {path}")


def paper_axes(ax):
    """Apply a simple manuscript-style axis format."""
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    ax.spines["left"].set_linewidth(1.0)
    ax.spines["bottom"].set_linewidth(1.0)
    ax.tick_params(direction="out", length=3.5, width=1.0, pad=3)


# ============================================================
# Hankel construction
# ============================================================

def build_observable_matrix_H_like_original(observed_data_matrix):
    """
    Build Hankel matrices exactly following the uploaded original script.

    For each observed row:
        num_rows = len(row_data) - delay * (embedding_dimension - 1)
        num_cols = delay * embedding_dimension

    Then stack all row-wise Hankel matrices vertically.
    """
    n_observed_rows = observed_data_matrix.shape[0]
    observable_matrix_blocks_H = []

    for i in range(n_observed_rows):
        row_data = observed_data_matrix[i, :]

        num_rows = len(row_data) - DELAY_TAU * (OBSERVABLE_WINDOW_LENGTH - 1)
        num_cols = DELAY_TAU * OBSERVABLE_WINDOW_LENGTH

        if num_rows <= 0:
            raise ValueError(
                f"Invalid Hankel shape: num_rows={num_rows}. "
                f"Check OBSERVABLE_WINDOW_LENGTH={OBSERVABLE_WINDOW_LENGTH}."
            )

        observable_matrix_block_H = np.zeros((num_rows, num_cols))

        for k in range(num_rows):
            for ell in range(OBSERVABLE_WINDOW_LENGTH):
                observable_matrix_block_H[k, ell * DELAY_TAU:(ell + 1) * DELAY_TAU] = row_data[k + ell * DELAY_TAU]

        observable_matrix_blocks_H.append(observable_matrix_block_H)

    return np.vstack(observable_matrix_blocks_H)


# ============================================================
# SVD and beta calculation
# ============================================================

def reconstruct_observable_matrix_H_by_original_svd(observable_matrix_H):
    """
    Reconstruct the Hankel matrix after retaining singular values according to
    the cumulative singular-value sum criterion used in the original script.
    """
    left_singular_vectors_U, singular_values, right_singular_vectors_Vh = np.linalg.svd(observable_matrix_H, full_matrices=False)

    total_sum = np.sum(singular_values)
    cumulative_sum = np.cumsum(singular_values)

    retained_rank_r = (
        np.argmax(cumulative_sum >= SVD_ENERGY_RATIO_BY_SINGULAR_VALUE_SUM * total_sum)
        + 1
    )

    print("retained_rank_r =", retained_rank_r)

    singular_values_r = singular_values[:retained_rank_r]
    left_singular_vectors_U_r = left_singular_vectors_U[:, :retained_rank_r]
    right_singular_vectors_Vh_r = right_singular_vectors_Vh[:retained_rank_r, :]

    reconstructed_observable_matrix_V = left_singular_vectors_U_r @ np.diag(singular_values_r) @ right_singular_vectors_Vh_r

    return reconstructed_observable_matrix_V, retained_rank_r


def compute_closure_defect_beta_like_original(reconstructed_observable_matrix_V, observable_velocity_matrix_V_dot):
    """
    Compute closure_defect_beta_values and A_t_column_norm_values using the same loop as the
    uploaded original script.
    """
    closure_defect_beta_values = []
    A_t_column_norm_values = []

    for i in range(0, OBSERVABLE_WINDOW_LENGTH - WINDOW_T, 1):
        start = i
        end = start + WINDOW_T

        V_dot_t = observable_velocity_matrix_V_dot[:, start:end]
        V_t = reconstructed_observable_matrix_V[:, start:end]

        A_t = V_dot_t @ np.linalg.pinv(V_t)

        A_t_column_norms = np.linalg.norm(A_t, axis=0)
        A_t_column_norm_mean = np.mean(A_t_column_norms)

        R_t = V_dot_t - A_t @ V_t
        R_t_row_norms = np.linalg.norm(R_t, axis=1)
        beta_t = np.mean(R_t_row_norms)

        A_t_column_norm_values.append(A_t_column_norm_mean)
        closure_defect_beta_values.append(beta_t)

    closure_defect_beta_values = np.asarray(closure_defect_beta_values, dtype=float)
    A_t_column_norm_values = np.asarray(A_t_column_norm_values, dtype=float)

    beta_mean_value = np.mean(closure_defect_beta_values)
    beta_variance_value = np.var(closure_defect_beta_values)
    variance_over_mean = beta_variance_value / beta_mean_value

    print("variance_over_mean =", variance_over_mean)

    return closure_defect_beta_values, A_t_column_norm_values


def find_local_maxima_like_original(closure_defect_beta_values, time_vector):
    """
    Find local maxima using the same window and threshold logic as the uploaded
    original script.
    """
    local_beta_maxima_values = []
    local_beta_maxima_indices = []
    local_beta_maxima_times = []

    window_size = LOCAL_MAXIMUM_SEARCH_WINDOW

    for i in range(window_size, len(closure_defect_beta_values) - window_size):
        window = closure_defect_beta_values[i - window_size:i + window_size + 1]
        max_index_in_window = np.argmax(window)

        if max_index_in_window == window_size:
            local_beta_maximum_value = window[window_size]

            if local_beta_maximum_value > LOCAL_MAXIMUM_BETA_THRESHOLD:
                local_beta_maxima_values.append(local_beta_maximum_value)
                local_beta_maxima_indices.append(i)
                local_beta_maxima_times.append(time_vector[i])

    local_beta_maxima_values = np.asarray(local_beta_maxima_values, dtype=float)
    local_beta_maxima_indices = np.asarray(local_beta_maxima_indices, dtype=int)
    local_beta_maxima_times = np.asarray(local_beta_maxima_times, dtype=float)

    for value, time_value in zip(local_beta_maxima_values, local_beta_maxima_times):
        print(f"local maximum: {value}, time: {time_value}")

    return local_beta_maxima_values, local_beta_maxima_indices, local_beta_maxima_times


