def plot_figS2_closure_defect_beta_and_hidden_z(closure_defect_beta_values, hidden_z_reference, closure_defect_beta_time, hidden_z_time):
    """
    Plot Fig. S2 using the same beta sequence as the uploaded original script.

    The hidden z coordinate is shown as a reference curve only.
    """
    fig = plt.figure(figsize=(6.2, 2.2), dpi=300, facecolor="white")
    ax = fig.add_axes([0.13, 0.28, 0.73, 0.64], facecolor="white")
    ax_right = ax.twinx()

    line_beta, = ax.plot(
        closure_defect_beta_time,
        closure_defect_beta_values,
        linestyle="-",
        label=rf"$\beta$ $(T={WINDOW_T})$",
        color="#C42238",
        alpha=0.85,
        linewidth=1.2
    )

    line_z, = ax_right.plot(
        hidden_z_time,
        hidden_z_reference,
        linestyle="-",
        label=r"$z$",
        color="#262626",
        alpha=0.55,
        linewidth=1.2
    )

    ax.set_ylabel(r"$\beta$", fontsize=16, color="black")
    ax_right.set_ylabel(r"$z$", fontsize=16, color="black")
    ax.set_xlabel(r"$t$", fontsize=16, color="black")

    ax.tick_params(axis="both", labelcolor="black", labelsize=14)
    ax_right.tick_params(axis="both", labelcolor="black", labelsize=14)

    ax.xaxis.set_major_locator(MaxNLocator(nbins=5))
    ax.yaxis.set_major_locator(MaxNLocator(nbins=4))
