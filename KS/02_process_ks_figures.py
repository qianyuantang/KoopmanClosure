def first_mode_a1_peak_valley_indices(first_mode_a1):
    """Return peak-valley extrema of first_mode_a1 for the WINDOW_T1 interval histogram."""
    peaks, _ = find_peaks(first_mode_a1, prominence=A1_PEAK_PROMINENCE, distance=A1_PEAK_DISTANCE)
    valleys, _ = find_peaks(-first_mode_a1, prominence=A1_PEAK_PROMINENCE, distance=A1_PEAK_DISTANCE)
    return np.sort(np.concatenate([peaks, valleys]))


def closure_defect_beta1_peak_indices(closure_defect_beta1):
    """Return closure_defect_beta1 peaks for the WINDOW_T2 interval histogram."""
    peaks, _ = find_peaks(closure_defect_beta1, prominence=BETA1_PEAK_PROMINENCE, distance=BETA1_PEAK_DISTANCE)
    return peaks


def interval_distribution(indices):
    """Return interval values and their counts."""
    if indices is None or len(indices) < 2:
        return np.array([], dtype=int), np.array([], dtype=int), np.array([], dtype=int)

    intervals = np.diff(np.asarray(indices)).astype(int)
    values, counts = np.unique(intervals, return_counts=True)
    return intervals, values, counts


