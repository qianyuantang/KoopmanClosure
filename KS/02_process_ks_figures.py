# ============================================================
# KS equation and modal velocity
# ============================================================

def ks_fourier_rhs(t, a, FOURIER_MODES=FOURIER_MODES, NU=NU):
    """Truncated Fourier-mode KS equation used to evaluate da/dt."""
    _ = t
    a_dot = np.zeros(FOURIER_MODES, dtype=np.float64)

    for k in range(1, FOURIER_MODES + 1):
        nonlinear_term = 0.0

        for m in range(-FOURIER_MODES, FOURIER_MODES + 1):
            if m > 0:
                a_m = a[m - 1]
            elif m < 0:
                a_m = -a[-m - 1]
            else:
                a_m = 0.0

            if 0 < k - m < FOURIER_MODES:
                a_km = a[k - m - 1]
            elif -FOURIER_MODES < k - m < 0:
                a_km = -a[m - k - 1]
            else:
                a_km = 0.0

            nonlinear_term += a_m * a_km

        a_dot[k - 1] = (k ** 2 - NU * k ** 4) * a[k - 1] - k * nonlinear_term

    return a_dot


def compute_modal_velocity(observable_matrix_H, analysis_time_grid_t):
    """Compute da/dt on the selected analysis segment."""
    observable_velocity_matrix_H_dot = np.zeros_like(observable_matrix_H)

    for i in range(observable_matrix_H.shape[1]):
        observable_velocity_matrix_H_dot[:, i] = ks_fourier_rhs(analysis_time_grid_t[i], observable_matrix_H[:, i], FOURIER_MODES, NU)

    return observable_velocity_matrix_H_dot


# ============================================================
# SVD reduction and closure defect
# ============================================================

def compute_reduced_coordinates_V(observable_matrix_H_input, observable_velocity_matrix_H_dot_input, retained_rank_r):
    """
    Apply SVD to the observable matrix and return reduced coordinates.

    H = U Sigma V^T
    reduced_coordinates_V = Sigma_r V_r^T
    dY/dt = left_singular_vectors_U_r^T dH/dt

    Here H is the raw 16-Fourier-mode observable matrix.
    """
    U, singular_values_all, Vh = np.linalg.svd(observable_matrix_H_input, full_matrices=False)

    left_singular_vectors_U_r = U[:, :retained_rank_r]
    singular_values_r = singular_values_all[:retained_rank_r]
    right_singular_vectors_Vh_r = Vh[:retained_rank_r, :]

    reduced_coordinates_V = np.diag(singular_values_r) @ right_singular_vectors_Vh_r
    reduced_velocity_V_dot = left_singular_vectors_U_r.T @ observable_velocity_matrix_H_dot_input

    return reduced_coordinates_V, reduced_velocity_V_dot


def prepare_window_sums_for_A_t_and_R_t(reduced_coordinates_V, reduced_velocity_V_dot):
    """Precompute cumulative matrices for sliding-window least-squares fits."""
    retained_rank_r, _ = reduced_coordinates_V.shape

    V_V_transpose_per_time = np.einsum("ik,jk->kij", reduced_coordinates_V, reduced_coordinates_V)
    V_dot_V_transpose_per_time = np.einsum("ik,jk->kij", reduced_velocity_V_dot, reduced_coordinates_V)

    cumulative_V_V_transpose = np.concatenate([np.zeros((1, retained_rank_r, retained_rank_r)), np.cumsum(V_V_transpose_per_time, axis=0)], axis=0)
    cumulative_V_dot_V_transpose = np.concatenate([np.zeros((1, retained_rank_r, retained_rank_r)), np.cumsum(V_dot_V_transpose_per_time, axis=0)], axis=0)
    cumulative_V_dot_squared_norm = np.concatenate([np.zeros((retained_rank_r, 1)), np.cumsum(reduced_velocity_V_dot ** 2, axis=1)], axis=1)

    return cumulative_V_V_transpose, cumulative_V_dot_V_transpose, cumulative_V_dot_squared_norm


def compute_closure_defect_beta_for_window_T(cumulative_V_V_transpose, cumulative_V_dot_V_transpose, cumulative_V_dot_squared_norm, window_length_T):
    """
    Compute local Koopman closure defect beta(t; T, r).

    Within each sliding window:
        A_t = dV V^dagger
        R = dV - A_t V
        beta = row-averaged ||R||_2
    """
    n_time_columns = cumulative_V_dot_squared_norm.shape[1] - 1

    if window_length_T >= n_time_columns:
        raise ValueError(
            f"window_length_T={window_length_T} is too large for sequence length {n_time_columns}."
        )

    closure_defect_beta_values = np.zeros(n_time_columns - window_length_T)

    for start in range(n_time_columns - window_length_T):
        end = start + window_length_T

        V_t_V_t_transpose = cumulative_V_V_transpose[end] - cumulative_V_V_transpose[start]
        V_dot_t_V_t_transpose = cumulative_V_dot_V_transpose[end] - cumulative_V_dot_V_transpose[start]
        V_dot_t_squared_norm_sum = cumulative_V_dot_squared_norm[:, end] - cumulative_V_dot_squared_norm[:, start]

        A_t = V_dot_t_V_t_transpose @ np.linalg.pinv(V_t_V_t_transpose)

        R_t_row_squared_norms = (
            V_dot_t_squared_norm_sum
            - 2.0 * np.einsum("ij,ij->i", A_t, V_dot_t_V_t_transpose)
            + np.einsum("ij,jk,ik->i", A_t, V_t_V_t_transpose, A_t)
        )

        closure_defect_beta_values[start] = np.mean(np.sqrt(np.maximum(R_t_row_squared_norms, 0.0)))

    return closure_defect_beta_values


