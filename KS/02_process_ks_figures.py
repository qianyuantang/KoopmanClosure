# ============================================================
# Main workflow
# ============================================================

def main():
    reset_output_dir()

    raw = load_raw_data()

    t = raw["t"]
    a = raw["a"]

    observable_matrix_H = a[:, ANALYSIS_START_INDEX:ANALYSIS_END_INDEX]
    analysis_time_grid_t = t[ANALYSIS_START_INDEX:ANALYSIS_END_INDEX]

    # Absolute raw simulation time is used for every x-axis.
    plot_time = analysis_time_grid_t

    first_mode_a1 = observable_matrix_H[0, :]

    print("Raw data shape:", a.shape)
    print("Analysis data shape:", observable_matrix_H.shape)
    print(f"Using manuscript parameters: WINDOW_T1={WINDOW_T1}, WINDOW_T2={WINDOW_T2}, RETAINED_RANK_R1={RETAINED_RANK_R1}, RETAINED_RANK_R2={RETAINED_RANK_R2}")
    print(f"Fig. 3(d) raw-time xlim = {FIG3D_TIME_MIN}:{FIG3D_TIME_MAX}")

    print("Computing modal velocities...")
    observable_velocity_matrix_H_dot = compute_modal_velocity(observable_matrix_H, analysis_time_grid_t)

    print("Computing closure_defect_beta1 with manuscript WINDOW_T1 and RETAINED_RANK_R1...")
    reduced_coordinates_V1, reduced_velocity_V1_dot = compute_reduced_coordinates_V(observable_matrix_H, observable_velocity_matrix_H_dot, RETAINED_RANK_R1)
    cumulative_V1_V1_transpose, cumulative_V1_dot_V1_transpose, cumulative_V1_dot_squared_norm = prepare_window_sums_for_A_t_and_R_t(reduced_coordinates_V1, reduced_velocity_V1_dot)
    closure_defect_beta1 = compute_closure_defect_beta_for_window_T(cumulative_V1_V1_transpose, cumulative_V1_dot_V1_transpose, cumulative_V1_dot_squared_norm, WINDOW_T1)

    print("Computing closure_defect_beta2 with manuscript WINDOW_T2 and RETAINED_RANK_R2...")
    reduced_coordinates_V2, reduced_velocity_V2_dot = compute_reduced_coordinates_V(observable_matrix_H, observable_velocity_matrix_H_dot, RETAINED_RANK_R2)
    cumulative_V2_V2_transpose, cumulative_V2_dot_V2_transpose, cumulative_V2_dot_squared_norm = prepare_window_sums_for_A_t_and_R_t(reduced_coordinates_V2, reduced_velocity_V2_dot)
    closure_defect_beta2 = compute_closure_defect_beta_for_window_T(cumulative_V2_V2_transpose, cumulative_V2_dot_V2_transpose, cumulative_V2_dot_squared_norm, WINDOW_T2)

    first_mode_a1_extrema = first_mode_a1_peak_valley_indices(first_mode_a1)
    closure_defect_beta1_peaks = closure_defect_beta1_peak_indices(closure_defect_beta1)

    plot_fig3b(first_mode_a1_extrema, closure_defect_beta1_peaks)
    plot_fig3c(closure_defect_beta2, first_mode_a1, plot_time)
    plot_fig3d(closure_defect_beta1, first_mode_a1, plot_time)

    print("Finished. SVG figures are saved in:", OUTPUT_DIR)


if __name__ == "__main__":
    main()
