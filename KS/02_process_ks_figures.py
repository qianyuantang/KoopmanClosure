def plot_fig3b(first_mode_a1_extrema, closure_defect_beta1_peaks):
    """Fig. 3(b): two-layer window-selection histograms."""
    _, values1, counts1 = interval_distribution(first_mode_a1_extrema)
    _, values2, counts2 = interval_distribution(closure_defect_beta1_peaks)

    fig = plt.figure(figsize=(10, 3), dpi=300, facecolor="white")
    ax1 = fig.add_axes([0.105, 0.23, 0.320, 0.68], facecolor="white")
    ax2 = fig.add_axes([0.569, 0.23, 0.320, 0.68], facecolor="white")

    label_size = 20
    tick_size = 20

    if values1.size > 0:
        ax1.bar(values1, counts1, width=0.9, edgecolor="black", linewidth=0.6, color="black")
        ax1.axvline(WINDOW_T1, linestyle="--", linewidth=1.1, color="black")
        ax1.set_ylim(0, max(int(counts1.max()), 1) * 1.20)
        ax1.set_ylabel("Count", fontsize=label_size, labelpad=8)
        ax1.tick_params(axis="both", labelsize=tick_size, labelcolor="black")
        ax1.xaxis.set_major_locator(MaxNLocator(nbins=4))
        ax1.yaxis.set_major_locator(MaxNLocator(nbins=3))
    else:
        ax1.text(0.5, 0.5, "Not enough peaks", ha="center", va="center",
                 fontsize=label_size, transform=ax1.transAxes)
        ax1.set_axis_off()

    if values2.size > 0:
        ax2.bar(values2, counts2, width=0.9, edgecolor="black", linewidth=0.6, color="black")
        ax2.axvline(WINDOW_T2, linestyle="--", linewidth=1.1, color="black")
        ax2.set_ylim(0, max(int(counts2.max()), 1) * 1.20)
        ax2.set_ylabel("Count", fontsize=label_size, labelpad=8)
        ax2.tick_params(axis="both", labelsize=tick_size, labelcolor="black")
        ax2.xaxis.set_major_locator(MaxNLocator(nbins=4))
        ax2.yaxis.set_major_locator(MaxNLocator(nbins=3))
    else:
        ax2.text(0.5, 0.5, "Not enough peaks", ha="center", va="center",
                 fontsize=label_size, transform=ax2.transAxes)
        ax2.set_axis_off()

    for ax in [ax1, ax2]:
        paper_axes(ax)
        ax.grid(False)

    fig.text(
        0.5,
        0.08,
        r"Peak interval $\Delta k$ (samples)",
        ha="center",
        va="center",
        fontsize=label_size
    )

    save_svg(fig, "fig3b_window_selection")
    plt.close(fig)


