def load_raw_data():
    """Load raw KS data generated by 01_generate_ks_raw_data.py."""
    if not os.path.exists(RAW_DATA_FILE):
        raise FileNotFoundError(
            f"{RAW_DATA_FILE} was not found. Run 01_generate_ks_raw_data.py first."
        )
    return np.load(RAW_DATA_FILE)


def reset_output_dir():
    """Delete old outputs and create a clean SVG-only output folder."""
    if os.path.isdir(OUTPUT_DIR):
        shutil.rmtree(OUTPUT_DIR)
    os.makedirs(OUTPUT_DIR, exist_ok=True)


def save_svg(fig, file_stem):
    """Save one figure as SVG only."""
    path = os.path.join(OUTPUT_DIR, f"{file_stem}.svg")
    fig.savefig(path, format="svg", facecolor="white", transparent=False)
    print(f"Saved: {path}")


def paper_axes(ax):
    """Single-axis paper style."""
    ax.spines["top"].set_visible(False)
